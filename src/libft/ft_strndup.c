#include "libft.h"

char	*ft_strndup(char const *s, size_t n)
{
	size_t	len;
	size_t	i;
	char	*dup;

	len = 0;
	while (s[len] && len < n)
		len++;
	dup = malloc(len + 1);
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dup[i] = s[i];
		i++;
	}
	dup[len] = '\0';
	return (dup);
}
